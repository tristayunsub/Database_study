SQL 조회 조건 파악.
조회 조건 컬럼에 따라 SQL의 Access Path가 결정되기 떄문이다.
효율적인 조회 조건 칼럼을 가진 테이블이 먼저 수행되게 하고, 선행 테이블에서
추출된 데이터와 조인 조건 컬럼을 파악 후 조인 방법과 데이터 액세스 방법이 결정되기 때문.

sql 조회 조건 분석
조회 조건 및 조인 컬럼의 효율성 확인을 위해 필요한 데이터를 조사한다.
각 조회 조건의 효율성을 확인하기 위해, 인덱스 존재 여부와 해당 인덱스 및 검색 조건,
조인 컬럼의 Cardinality나 Distinct Value등을 확인해야한다.

조회 조건의 효율성을 확인할 수 있는 정보를 수집해야 Dynamic SQL에 대한 성능 개선안을 도출 할 수 있기 때문이다.

조회 조건별 효율성 체크.
가장 중요한 부분은 모든 조회 조건 컬럼들의 효율 순서를 정하는 것.

도출된 순서가 바로 조인 순서가 된다.

추가 인덱스 생성 고려
모든 정보를 토대로 효율적인 조회가 가능한 컬럼 중 인덱스가 존재하지 않은 컬럼을 선별한다.
수급자명 조건인 TBF42 테이블의 sgj_nm 컬럼에 인덱스가 생성되어 잇지 않다.
해당 칼럼은 = 조회시 평균 51건만 추출하여, 인덱스 생성 시 효율적인 조회가 될 수 있었음을 알 수 있으므로, 추가적인 개선안으로
해당 칼럼에 인덱스 생성을 고려한다.

