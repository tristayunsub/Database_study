Dynamic SQL이란?
SQL의 특정 검색 조건에 따라 SQL이 유연하게 변경되는 방식
화면의 조회 조건에 따라 SQL에 조건을 추가하는 방식
보통 웹프로그램에서 XML을 활용하여 SQL에 DYNACMIC하게추가.

